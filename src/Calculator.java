public class Calculator {
    public static int sum(int var1, int var2)
    {
        return var1+var2;
    }
    public static int minus(int variable1, int variable2)
    {
        return variable1-variable2;
    }
    public static int round(int one, int two)
    {
        return one*two;
    }
    public static double divide(int from, int to)
    {
        return (double) from/to;
    }
}
